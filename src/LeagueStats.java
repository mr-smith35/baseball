import java.io.*;

public class LeagueStats {
	int games;
	int wins;
	
	int last10Wins;
	int last10Losses;
	
	String streak;
	String[] last3GamePitchers;
	
	public LeagueStats(int games, int wins, int last10Wins, String streak,
			String[] last3GamePitchers) {
		super();
		this.games = games;
		this.wins = wins;
		this.last10Wins = last10Wins;
		this.last10Losses = last10Losses;
		this.streak = streak;
		this.last3GamePitchers = last3GamePitchers;
	}
	

	


	
	
	
}
